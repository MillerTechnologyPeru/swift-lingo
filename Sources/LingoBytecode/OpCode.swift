    case or = 0x13
    case not = 0x14
    case containsStr = 0x15
    case contains0Str = 0x16
    case getChunk = 0x17
    case hiliteChunk = 0x18
    case ontoSpr = 0x19
    case intoSpr = 0x1a
    case getField = 0x1b
    case startTell = 0x1c
    case endTell = 0x1d
    case pushList = 0x1e
    case pushPropList = 0x1f
    case swap = 0x21
    case callJavaScript = 0x26

    // multi-byte (0x40–0xFF)
    case pushInt8 = 0x41
    case pushArgListNoRet = 0x42
    case pushArgList = 0x43
    case pushCons = 0x44
    case pushSymb = 0x45
